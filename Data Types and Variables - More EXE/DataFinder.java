package JavaFundamentalsExe;

import java.util.Scanner;

public class DataFinder {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);


        if (in.hasNextInt()){
            System.out.println("this is int");
        }else if (in.hasNextBoolean()){
            System.out.println("this is int");
        }
    }
}
